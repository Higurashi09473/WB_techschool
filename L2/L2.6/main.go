package main

import (
	"fmt"
)

func main() {
	// Инициализируем слайс: ["1", "2", "3"] (cap = 3, len = 3)
	var s = []string{"1", "2", "3"}

	// Передаём копию заголовка слайса в функцию
	modifySlice(s)

	// Вывод: [3 2 3]
	fmt.Println(s)
}

func modifySlice(i []string) {
	// Изменяем элемент по индексу, меняется оригинальный массив (указатель тот же)
	// Теперь оригинальный s: ["3", "2", "3"]
	i[0] = "3"
	// Append: len=3, cap=3 → перевыделение памяти!
	// i теперь — НОВЫЙ слайс с НОВЫМ указателем на ["3", "2", "3", "4"]
	// Оригинальный s НЕ меняется.
	i = append(i, "4")
	// Оригинальный s НЕ меняется.
	i[1] = "5"
	// Оригинальный s НЕ меняется.]]]]
	i = append(i, "6")
}
